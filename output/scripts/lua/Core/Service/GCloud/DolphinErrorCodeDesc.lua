function DolphinErrorCodeDesc:AddPrefix(ErrorCode)
  return "D" .. ErrorCode
end

function DolphinErrorCodeDesc:GetDesc(ErrorCode)
  local FoundText = ErrorCodeMapping[ErrorCode]
  if not string.IsNilOrEmpty(FoundText) then
    return string.format(FoundText, self:AddPrefix(ErrorCode))
  elseif ErrorCode >= 355467264 and ErrorCode <= 356515839 then
    return string.format(Text.PermissionError, self:AddPrefix(ErrorCode))
  elseif ErrorCode >= 287309824 and ErrorCode <= 288358399 then
    return string.format(Text.FileError, self:AddPrefix(ErrorCode))
  elseif ErrorCode >= 420478976 and ErrorCode <= 421527551 then
    return string.format(Text.FileError, self:AddPrefix(ErrorCode))
  elseif ErrorCode >= 487587840 and ErrorCode <= 488636415 then
    return string.format(Text.FileError, self:AddPrefix(ErrorCode))
  else
    return string.format(Text.UnknownError, self:AddPrefix(ErrorCode))
  end
end

return DolphinErrorCodeDesc
