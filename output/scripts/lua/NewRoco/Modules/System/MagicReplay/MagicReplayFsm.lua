local MagicReplayModuleCmd = require("NewRoco.Modules.System.MagicReplay.MagicReplayModuleCmd")
local MagicReplayModuleEvent = require("NewRoco.Modules.System.MagicReplay.MagicReplayModuleEvent")
local MagicReplayFsmState = require("NewRoco.Modules.System.MagicReplay.State.MagicReplayFsmState")
local Fsm = require("NewRoco.Modules.Core.Fsm.Fsm")
local MagicReplayInitAction = require("NewRoco.Modules.System.MagicReplay.Action.MagicReplayInitAction")
local MagicReplayIdleAction = require("NewRoco.Modules.System.MagicReplay.Action.MagicReplayIdleAction")
local MagicReplayShareUIAction = require("NewRoco.Modules.System.MagicReplay.Action.MagicReplayShareUIAction")
local MagicReplayBlockInputAction = require("NewRoco.Modules.System.MagicReplay.Action.MagicReplayBlockInputAction")
local MagicReplayDestroyAction = require("NewRoco.Modules.System.MagicReplay.Action.MagicReplayDestroyAction")
local MagicReplayPreviewPrepareAction = require("NewRoco.Modules.System.MagicReplay.Action.MagicReplayPreviewPrepareAction")
local MagicReplayPreviewMainAction = require("NewRoco.Modules.System.MagicReplay.Action.MagicReplayPreviewMainAction")
local MagicReplayRecordPrepareStageAction = require("NewRoco.Modules.System.MagicReplay.Action.MagicReplayRecordPrepareStageAction")
local MagicReplayRecordCountDownAction = require("NewRoco.Modules.System.MagicReplay.Action.MagicReplayRecordCountDownAction")
local MagicReplayRecordMainAction = require("NewRoco.Modules.System.MagicReplay.Action.MagicReplayRecordMainAction")
local MagicReplayRecordProcessResultAction = require("NewRoco.Modules.System.MagicReplay.Action.MagicReplayRecordProcessResultAction")
local MagicReplayReplayDownloadAction = require("NewRoco.Modules.System.MagicReplay.Action.MagicReplayReplayDownloadAction")
local MagicReplayReplayPrepareStageAction = require("NewRoco.Modules.System.MagicReplay.Action.MagicReplayReplayPrepareStageAction")
local MagicReplayReplayMainAction = require("NewRoco.Modules.System.MagicReplay.Action.MagicReplayReplayMainAction")
local MagicReplayReplayIdleAction = require("NewRoco.Modules.System.MagicReplay.Action.MagicReplayReplayIdleAction")
local MagicReplayShareVideoMainAction = require("NewRoco.Modules.System.MagicReplay.Action.MagicReplayShareVideoMainAction")
local MagicReplayOpenWhiteScreenAction = require("NewRoco.Modules.System.MagicReplay.Action.MagicReplayOpenWhiteScreenAction")
local MagicReplayCloseWhiteScreenAction = require("NewRoco.Modules.System.MagicReplay.Action.MagicReplayCloseWhiteScreenAction")
local MagicReplayHidePlayerAndPetAction = require("NewRoco.Modules.System.MagicReplay.Action.MagicReplayHidePlayerAndPetAction")
local MagicReplayShowPlayerAndPetAction = require("NewRoco.Modules.System.MagicReplay.Action.MagicReplayShowPlayerAndPetAction")
local MagicReplayStartPreviewCamAction = require("NewRoco.Modules.System.MagicReplay.Action.MagicReplayStartPreviewCamAction")
local MagicReplayStopPreviewCamAction = require("NewRoco.Modules.System.MagicReplay.Action.MagicReplayStopPreviewCamAction")
local MagicReplayFsm = Fsm:Extend("MagicReplayFsm")

local function CreateFsm()
  local FINISHED = "FINISHED"
  local fsm = Fsm("MagicReplayFsm")
  local CurrentOption = fsm:CreateVar("CurrentOption", nil)
  local ParentModule = fsm:CreateVar("ParentModule", nil)
  local IsReconnect = fsm:CreateVar("bIsReconnect", false)
  local OpType = fsm:CreateVar("OpType", 0)
  local CurrentOpType = fsm:CreateVar("CurrentOpType", 0)
  local RecordStartPlayerPos = fsm:CreateVar("RecordStartPlayerPos", {})
  local ReplayFileName = fsm:CreateVar("ReplayFileName", "")
  local NeedWhiteScreen = fsm:CreateVar("NeedWhiteScreen", false)
  local ReplayNpcId = fsm:CreateVar("ReplayNpcId", 0)
  local InitState = fsm:CreateSequentialState("InitState")
  local RecordPrepareState = MagicReplayFsmState("RecordPrepareState")
  fsm:AddState(RecordPrepareState)
  local RecordProcessState = MagicReplayFsmState("RecordProcessState")
  fsm:AddState(RecordProcessState)
  local PreviewPrepareState = MagicReplayFsmState("PreviewPrepareState")
  fsm:AddState(PreviewPrepareState)
  local PreviewProcessState = MagicReplayFsmState("PreviewProcessState")
  fsm:AddState(PreviewProcessState)
  local ShareState = MagicReplayFsmState("ShareState")
  fsm:AddState(ShareState)
  local ReplayPrepareState = MagicReplayFsmState("ReplayPrepareState")
  fsm:AddState(ReplayPrepareState)
  local ReplayProcessState = MagicReplayFsmState("ReplayProcessState")
  fsm:AddState(ReplayProcessState)
  local ReplayIdleState = MagicReplayFsmState("ReplayIdleState")
  fsm:AddState(ReplayIdleState)
  local ShareVideoState = MagicReplayFsmState("ShareVideoState")
  fsm:AddState(ShareVideoState)
  local IdleState = fsm:CreateSequentialState("IdleState")
  local EndState = fsm:CreateSequentialState("EndState")
  InitState:AddAction(MagicReplayInitAction("Init", {OpType = OpType, CurrentOpType = CurrentOpType}))
  IdleState:AddAction(MagicReplayIdleAction("Idle", {CurrentOpType = CurrentOpType}))
  RecordPrepareState:AddAction(MagicReplayStopPreviewCamAction("StopPreviewCam", {ParentModule = ParentModule}))
  RecordPrepareState:AddAction(MagicReplayCloseWhiteScreenAction("CloseWhiteScreen", {ParentModule = ParentModule}))
  RecordPrepareState:AddAction(MagicReplayShowPlayerAndPetAction("ShowPlayerAndPet", {ParentModule = ParentModule}))
  RecordPrepareState:AddAction(MagicReplayRecordPrepareStageAction("RecordPrepareStage", {ParentModule = ParentModule, CurrentOpType = CurrentOpType}))
  RecordProcessState:AddAction(MagicReplayRecordCountDownAction("RecordCountDown", {ParentModule = ParentModule, CurrentOpType = CurrentOpType}))
  RecordProcessState:AddAction(MagicReplayRecordMainAction("RecordMain", {ParentModule = ParentModule, RecordStartPlayerPos = RecordStartPlayerPos}))
  RecordProcessState:AddAction(MagicReplayOpenWhiteScreenAction("OpenWhiteScreen", {ParentModule = ParentModule, NeedWhiteScreen = true}))
  RecordProcessState:AddAction(MagicReplayRecordProcessResultAction("RecordProcessResult", {ParentModule = ParentModule, NeedWhiteScreen = NeedWhiteScreen}))
  PreviewPrepareState:AddAction(MagicReplayOpenWhiteScreenAction("OpenWhiteScreen", {ParentModule = ParentModule, NeedWhiteScreen = NeedWhiteScreen}))
  PreviewPrepareState:AddAction(MagicReplayHidePlayerAndPetAction("HidePlayerAndPet", {ParentModule = ParentModule}))
  PreviewPrepareState:AddAction(MagicReplayPreviewPrepareAction("PreviewPrepare", {ParentModule = ParentModule, CurrentOpType = CurrentOpType}))
  PreviewPrepareState:AddAction(MagicReplayStopPreviewCamAction("StopPreviewCam", {ParentModule = ParentModule}))
  PreviewPrepareState:AddAction(MagicReplayStartPreviewCamAction("StartPreviewCam", {ParentModule = ParentModule}))
  PreviewPrepareState:AddAction(MagicReplayCloseWhiteScreenAction("CloseWhiteScreen", {ParentModule = ParentModule}))
  PreviewProcessState:AddAction(MagicReplayPreviewMainAction("PreviewMain", {ParentModule = ParentModule, NeedWhiteScreen = NeedWhiteScreen}))
  PreviewProcessState:AddAction(MagicReplayOpenWhiteScreenAction("OpenWhiteScreen", {ParentModule = ParentModule, NeedWhiteScreen = NeedWhiteScreen}))
  ShareState:AddAction(MagicReplayShowPlayerAndPetAction("ShowPlayerAndPet", {ParentModule = ParentModule}))
  ShareState:AddAction(MagicReplayStopPreviewCamAction("StopPreviewCam", {ParentModule = ParentModule}))
  ShareState:AddAction(MagicReplayCloseWhiteScreenAction("CloseWhiteScreen", {ParentModule = ParentModule}))
  ShareState:AddAction(MagicReplayShareUIAction("Share", {
    ParentModule = ParentModule,
    CurrentOpType = CurrentOpType,
    NeedWhiteScreen = NeedWhiteScreen
  }))
  ReplayPrepareState:AddAction(MagicReplayCloseWhiteScreenAction("CloseWhiteScreen", {ParentModule = ParentModule}))
  ReplayPrepareState:AddAction(MagicReplayReplayDownloadAction("ReplayDownload", {
    ParentModule = ParentModule,
    CurrentOpType = CurrentOpType,
    ReplayFileName = ReplayFileName
  }))
  ReplayPrepareState:AddAction(MagicReplayReplayPrepareStageAction("ReplayPrepareStage", {ParentModule = ParentModule}))
  ReplayProcessState:AddAction(MagicReplayReplayMainAction("ReplayMain", {ParentModule = ParentModule, ReplayFileName = ReplayFileName}))
  ReplayIdleState:AddAction(MagicReplayReplayIdleAction("ReplayIdle", {ParentModule = ParentModule}))
  ShareVideoState:AddAction(MagicReplayCloseWhiteScreenAction("CloseWhiteScreen", {ParentModule = ParentModule}))
  ShareVideoState:AddAction(MagicReplayHidePlayerAndPetAction("HidePlayerAndPet", {ParentModule = ParentModule}))
  ShareVideoState:AddAction(MagicReplayStartPreviewCamAction("StartPreviewCam", {ParentModule = ParentModule}))
  ShareVideoState:AddAction(MagicReplayShareVideoMainAction("ShareVideoMain", {ParentModule = ParentModule}))
  ShareVideoState:AddAction(MagicReplayStopPreviewCamAction("StopPreviewCam", {ParentModule = ParentModule}))
  ShareVideoState:AddAction(MagicReplayShowPlayerAndPetAction("ShowPlayerAndPet", {ParentModule = ParentModule}))
  EndState:AddAction(MagicReplayStopPreviewCamAction("StopPreviewCam", {ParentModule = ParentModule}))
  EndState:AddAction(MagicReplayDestroyAction("Destroy", {
    bIsReconnect = IsReconnect,
    ParentModule = ParentModule,
    ReplayNpcId = ReplayNpcId
  }))
  InitState:AddTransitionToState(FINISHED, IdleState)
  IdleState:AddTransitionToState(FINISHED, IdleState)
  IdleState:AddTransitionToState(MagicReplayModuleEvent.EnterRecordState, RecordPrepareState)
  IdleState:AddTransitionToState(MagicReplayModuleEvent.EnterReplayState, ReplayPrepareState)
  IdleState:AddTransitionToState(MagicReplayModuleEvent.EnterPreviewState, PreviewPrepareState)
  RecordPrepareState:AddTransitionToState(MagicReplayModuleEvent.EnterRecordProcessState, RecordProcessState)
  RecordProcessState:AddTransitionToState(FINISHED, PreviewPrepareState)
  PreviewPrepareState:AddTransitionToState(FINISHED, PreviewProcessState)
  PreviewProcessState:AddTransitionToState(FINISHED, ShareState)
  ReplayPrepareState:AddTransitionToState(FINISHED, ReplayProcessState)
  ReplayProcessState:AddTransitionToState(FINISHED, ReplayIdleState)
  ShareVideoState:AddTransitionToState(FINISHED, ReplayIdleState)
  fsm:AddTransitionToState(MagicReplayModuleEvent.EnterIdleState, IdleState)
  fsm:AddTransitionToState(MagicReplayModuleEvent.EnterRecordState, RecordPrepareState)
  fsm:AddTransitionToState(MagicReplayModuleEvent.EnterPreviewState, PreviewPrepareState)
  fsm:AddTransitionToState(MagicReplayModuleEvent.EnterReplayState, ReplayPrepareState)
  fsm:AddTransitionToState(MagicReplayModuleEvent.EnterShareVideoState, ShareVideoState)
  fsm:AddTransitionToState(MagicReplayModuleEvent.EnterEndState, EndState)
  fsm:SetInitState(InitState)
  EndState.isFinalState = true
  return fsm
end

return CreateFsm
