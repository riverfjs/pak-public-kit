local PGCModuleHead = NRCModuleHeadBase:Extend("PGCModuleHead")

function PGCModuleHead:OnConstruct()
  _G.PGCModuleCmd = reload("NewRoco.Modules.System.PGC.PGCModuleCmd")
  _G.PGCModuleEnum = reload("NewRoco.Modules.System.PGC.PGCModuleEnum")
  _G.PGCModuleData = reload("NewRoco.Modules.System.PGC.PGCModuleData")
  self:BindCmd(_G.PGCModuleCmd.OpenDataView, "OnOpenDataView")
  self:BindCmd(_G.PGCModuleCmd.CloseDataView, "OnCloseDataView")
  self:BindCmd(_G.PGCModuleCmd.LoadRecord, "OnLoadRecord")
  self:BindCmd(_G.PGCModuleCmd.SaveRecord, "OnSaveRecord")
  self:BindCmd(_G.PGCModuleCmd.CloseRecord, "OnCloseRecord")
  self:BindCmd(_G.PGCModuleCmd.AddRecord, "OnAddRecord")
  self:BindCmd(_G.PGCModuleCmd.RemoveRecord, "OnRemoveRecord")
  self:BindCmd(_G.PGCModuleCmd.CreateNPC, "OnCreateNPC")
  self:BindCmd(_G.PGCModuleCmd.RefreshDataState, "OnRefreshDataState")
  self:BindCmd(_G.PGCModuleCmd.ShowDataSummary, "OnShowDataSummary")
  self:BindCmd(_G.PGCModuleCmd.ShowDataDetail, "OnShowDataDetail")
  self:BindCmd(_G.PGCModuleCmd.ShowEnumData, "OnShowEnumData")
  self:BindCmd(_G.PGCModuleCmd.SimulateServerNPCEnter, "OnSimulateServerNPCEnter")
  self:BindCmd(_G.PGCModuleCmd.SimulateServerNPCLeave, "OnSimulateServerNPCLeave")
  self:BindCmd(_G.PGCModuleCmd.SimulateServerOptionChange, "OnSimulateServerOptionChange")
  self:BindCmd(_G.PGCModuleCmd.SimulateServerNextAction, "OnSimulateServerNextAction")
end

return PGCModuleHead
