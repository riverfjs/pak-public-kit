local a = require("Common.Coroutine.async")
local au = require("Common.Coroutine.async_util")
local test_cases = {}
test_cases.InstanceStructUsage = a.task(function()
  print("=== TEST CASE: InstanceStructUsage ===")
  UE.FInstanceStructBuilder("StateParam_Test"):AddFieldInt("TestInt"):AddFieldFloat("TestFloat"):AddFieldString("TestString"):AddFieldString("TestString2"):Build()
  local data = UE.FInstanceStructPtr("StateParam_Test")
  data.TestString = "Idle"
  data:SetString("TestString2", "Happy")
  data.TestInt = 1
  data.TestFloat = 1.2
  print(data.TestString, "Idle")
  print(data:GetString("TestString"), "Idle")
  print(data.TestString, "Idle")
  print(data.TestString2, "Happy")
  print(data:GetString("TestString2"), "Happy")
  print(data.TestString2, "Happy")
  print(data.TestInt, 1)
  print(data:GetFloat("TestInt"), 1)
  print(data.TestInt, 1)
  print(data.TestFloat, 1.2)
  print(data:GetFloat("TestFloat"), 1.2)
  print(data.TestFloat, 1.2)
end)
test_cases.CreateAndDestroy = a.task(function()
  print("=== TEST CASE: CreateAndDestroy ===")
  print("[op]create container")
  local Container = NewObject(UE.UAIStateContainer)
  local ContainerRef = UnLua.Ref(Container)
  Container:LuaSetDebugging(true)
  a.wait(au.NextTick())
  print("[op]add state 9000001")
  Container:LuaAddState(9000001, AIDefines.DummyInstData)
  print("[check.1]", Container:DebugStateIds())
  a.wait(au.DelaySeconds(1.4))
  print("[check.2]", Container:DebugStateIds())
  a.wait(au.DelaySeconds(1))
  print("[op]add state 9000003")
  Container:LuaAddState(9000003, AIDefines.DummyInstData)
  print("[check.3]", Container:DebugStateIds())
  a.wait(au.DelaySeconds(2))
  print("[check.4]", Container:DebugStateIds())
  print("[op]clean up")
  Container:LuaCleanUp()
  print("[check.clear]", Container:DebugStateIds())
  if UE.UObject.IsValid(ContainerRef) then
    UnLua.Unref(ContainerRef)
  end
end)
