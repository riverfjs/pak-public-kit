test_cases.Remote = a.task(function()
  print("=== TEST CASE: Remote ===")
  print("[op]create container")
  local Container = NewObject(UE.UAIStateContainer)
  local ContainerRef = UnLua.Ref(Container)
  Container:LuaSetDebugging(true)
  Container:LuaSetAuth(2)
  a.wait(au.NextTick())
  print("[op]add state 9000001")
  Container:LuaNtyAddState(2, 9000001, AIDefines.DummyInstData)
  print("[check.1]", Container:DebugStateIds())
  a.wait(au.DelaySeconds(3))
  print("[check.1]", Container:DebugStateIds())
  print("[op]add state 9000002")
  Container:LuaNtyAddState(3, 9000002, AIDefines.DummyInstData)
  print("[check.1]", Container:DebugStateIds())
  a.wait(au.DelaySeconds(1))
  print("[check.1]", Container:DebugStateIds())
  print("[op]scope +")
  do
    local _ <close> = Container:StateScopeLock()
    print("[op]remove state 9000002")
    Container:LuaNtyRemoveState(3, 9000002)
    print("[check.1]", Container:DebugStateIds())
    print("[op]add state 9000003")
    Container:LuaNtyAddState(4, 9000003, AIDefines.DummyInstData)
    print("[check.1]", Container:DebugStateIds())
    print("[op]scope -")
  end
  print("[check.1]", Container:DebugStateIds())
  a.wait(au.DelaySeconds(2))
  print("[check.1]", Container:DebugStateIds())
  print("[op]clean up")
  Container:LuaCleanUp()
  print("[check.clear]", Container:DebugStateIds())
  if UE.UObject.IsValid(ContainerRef) then
    UnLua.Unref(ContainerRef)
  end
end)
return test_cases
